public class MyChatPagerAdapter extends FragmentPagerAdapter {
    public final int COUNT = 2;
    private String[] titles = new String[]{"好友", "未关注"};
    private Context context;

    public MyChatPagerAdapter(FragmentManager fm, Context context) {
        super(fm);
        this.context = context;
    }

    @Override
    public Fragment getItem(int position) {
        if (position == 0) {
            return new ChatFriendsFragment();
        } else if (position == 1) {
            return new ChatNoFriendsFragment();
        }
        return new ChatFriendsFragment();
    }


    @Override
    public int getCount() {
        return COUNT;
    }

    @Override
    public CharSequence getPageTitle(int position) {
        return titles[position];
    }

}
